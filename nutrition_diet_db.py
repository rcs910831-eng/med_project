def get_cancer_diet(cancer_name: str) -> dict:
    """암종명으로 특화 식단 가이드 반환."""
    key = CANCER_DIET_MAP.get(cancer_name, CANCER_DIET_MAP.get(cancer_name.replace(" ", ""), None))
    if key and key in CANCER_TYPE_DIET:
        return CANCER_TYPE_DIET[key]
    # 부분 매칭
    for k in CANCER_TYPE_DIET:
        if cancer_name in k or k in cancer_name:
            return CANCER_TYPE_DIET[k]
    return {}


def get_chronic_diet(disease_name: str) -> dict:
    """만성질환명으로 식단 가이드 반환."""
    key = CHRONIC_DIET_MAP.get(disease_name, CHRONIC_DIET_MAP.get(disease_name.strip(), None))
    if key and key in CHRONIC_DISEASE_DIET:
        return CHRONIC_DISEASE_DIET[key]
    for k in CHRONIC_DISEASE_DIET:
        if disease_name in k or k in disease_name:
            return CHRONIC_DISEASE_DIET[k]
    return {}


def search_diet(query: str) -> list:
    """식품명, 질환명, 영양소로 통합 검색."""
    q = query.lower().strip()
    results = []

    # 암 식단 검색
    for name, data in CANCER_TYPE_DIET.items():
        if q in name.lower() or q in str(data).lower():
            results.append({"type": "암식단", "name": name, "data": data,
                             "출처": data.get("출처", [])})

    # 만성질환 식단 검색
    for name, data in CHRONIC_DISEASE_DIET.items():
        if q in name.lower() or q in str(data).lower():
            results.append({"type": "만성질환식단", "name": name, "data": data,
                             "출처": data.get("출처", [])})

    # 공통 가이드 검색
    if q in str(CANCER_NUTRITION_GENERAL).lower():
        results.append({"type": "암공통식단", "name": "암환자공통가이드",
                         "data": CANCER_NUTRITION_GENERAL,
                         "출처": CANCER_NUTRITION_GENERAL["출처"]})

    results.sort(key=lambda x: 0 if x["type"] == "암식단" else 1)
    return results[:6]


def get_food_restrictions(disease: str) -> dict:
    """질환별 금기/권장 식품 요약 반환."""
    diet = get_cancer_diet(disease) or get_chronic_diet(disease)
    if not diet:
        return {}

    restrictions = {"권장": [], "제한": [], "출처": diet.get("출처", [])}

    def _extract(d, depth=0):
        if depth > 3:
            return
        if isinstance(d, dict):
            for k, v in d.items():
                if any(w in k for w in ["권장", "좋은", "추천", "허용"]):
                    if isinstance(v, list):
                        restrictions["권장"].extend(v)
                    elif isinstance(v, str):
                        restrictions["권장"].append(v)
                elif any(w in k for w in ["금기", "제한", "피해야", "금지", "나쁜"]):
                    if isinstance(v, list):
                        restrictions["제한"].extend(v)
                    elif isinstance(v, str):
                        restrictions["제한"].append(v)
                else:
                    _extract(v, depth + 1)
        elif isinstance(d, list):
            pass

    _extract(diet)
    return restrictions


