def _get_pools(patient_id: str):
    """환자 ID → 아침/점심/저녁/간식 풀 반환"""
    p = PATIENT_PERSONALIZED_DIET.get(patient_id)
    if not p:
        return (["두유", "야채죽"], ["생선구이", "쌀밥"], ["닭가슴살", "채소"], ["바나나"])

    cancer = p.get("cancer")
    disease = p.get("disease")
    base = CANCER_DIET.get(cancer) if cancer else None
    chronic = CHRONIC_DISEASE_DIET.get(disease) if disease else None

    if base:
        foods = base["권장_음식"]
        bp = foods.get("아침", ["두유", "야채죽"])
        lp = foods.get("점심", ["생선구이", "맑은 국"])
        dp = foods.get("저녁", ["닭가슴살", "채소"])
        sp = foods.get("간식", ["바나나", "요구르트"])
    elif chronic:
        r = chronic["권장_음식"]
        bp, lp, dp, sp = r[:2], r[2:4], r[1:3], r[-2:]
    else:
        bp, lp, dp, sp = ["두유", "야채죽"], ["일반식"], ["일반식"], ["과일"]

    # 만성질환 병합: 금기 음식 추가 반영
    avoid = []
    if base:
        avoid += base.get("피해야할_음식", [])
    if chronic:
        avoid += chronic.get("피해야할_음식", [])

    return bp, lp, dp, sp, avoid


def generate_weekly_diet(patient_id: str) -> dict:
    """환자별 1주일 맞춤 식단 (정적)"""
    p = PATIENT_PERSONALIZED_DIET.get(patient_id)
    if not p:
        return None

    bp, lp, dp, sp, avoid = _get_pools(patient_id)
    source = ""
    if p.get("cancer") and p["cancer"] in CANCER_DIET:
        source = CANCER_DIET[p["cancer"]].get("출처", "국립암센터 가이드라인")
    return {
        "환자명": p["name"],
        "암종":   p.get("cancer") or "없음",
        "만성질환": p.get("disease") or "없음",
        "아침": bp[:3] if len(bp) >= 3 else bp,
        "점심": lp[:3] if len(lp) >= 3 else lp,
        "저녁": dp[:3] if len(dp) >= 3 else dp,
        "간식": sp[:2] if len(sp) >= 2 else sp,
        "피해야할_음식": avoid[:5] if len(avoid) >= 5 else avoid,
        "출처": source
    }


def generate_monthly_diet(patient_id: str, year: int = 2026, month: int = 5) -> list:
    """환자별 31일 매일 완전히 다른 식단 생성 (시드 고도화)"""
    p = PATIENT_PERSONALIZED_DIET.get(patient_id, {})
    cancer = p.get("cancer")
    disease = p.get("disease")
    
    # 기본 가이드라인 가져오기
    base = CANCER_DIET.get(cancer, {})
    chronic = CHRONIC_DISEASE_DIET.get(disease, {})
    
    # 가이드라인 기반 풀 구성
    guideline_breakfast = base.get("권장_음식", {}).get("아침", []) + chronic.get("권장_음식", [])[:2]
    guideline_lunch = base.get("권장_음식", {}).get("점심", []) + chronic.get("권장_음식", [])[2:4]
    guideline_dinner = base.get("권장_음식", {}).get("저녁", []) + chronic.get("권장_음식", [])[1:3]
    guideline_snacks = base.get("권장_음식", {}).get("간식", []) + chronic.get("권장_음식", [])[-2:]
    
    # 전체 메뉴 풀과 병합 (다양성 극대화)
    breakfast_pool = list(set(guideline_breakfast + MENU_POOL["곡류"][:3] + ["요거트", "삶은계란"]))
    lunch_pool = list(set(guideline_lunch + MENU_POOL["곡류"] + MENU_POOL["단백질"]))
    dinner_pool = list(set(guideline_dinner + MENU_POOL["단백질"] + MENU_POOL["채소"]))
    snack_pool = list(set(guideline_snacks + MENU_POOL["간식"]))
    
    avoid = list(set(base.get("피해야할_음식", []) + chronic.get("피해야할_음식", [])))
    source = base.get("출처", "국립암센터·서울아산병원 가이드라인")
    
    monthly = []
    for day in range(1, 32):
        # 환자ID + 날짜별 유니크 시드 생성
        random.seed(hash(f"{patient_id}-{year}-{month}-{day}-PRO") % (2**31))
        
        # 매일 다른 2-3가지 메뉴 선택
        daily_b = random.sample(breakfast_pool, min(3, len(breakfast_pool)))
        daily_l = random.sample(lunch_pool, min(3, len(lunch_pool)))
        daily_d = random.sample(dinner_pool, min(3, len(dinner_pool)))
        daily_s = random.sample(snack_pool, min(2, len(snack_pool)))
        
        monthly.append({
            "day": day,
            "아침": daily_b,
            "점심": daily_l,
            "저녁": daily_d,
            "간식": daily_s,
            "피해야할_음식": avoid[:4] if avoid else ["자극적 음식"],
            "출처": source
        })
    return monthly
